
public class Location {
	Double x;
	Double y;
	Integer map_id;
	
	public Location(Double x, Double y, Integer map_id){
		this.x = x;
		this.y = y;
		this.map_id = map_id;
	}

	public Double getX() {
		return x;
	}

	public Double getY() {
		return y;
	}

	public Integer getMap_id() {
		return map_id;
	}
	
	
}
